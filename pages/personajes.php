<section class="personajes">
    <article class="personaje">
        <img src="../media/img/luke-skywalker.jpg" alt="Luke Skywalker">
        <h2>Luke Skywalker</h2>
        <p>El héroe que enfrenta a Darth Vader y al Imperio, y quien finalmente restaura la paz en la galaxia al
            derrotar a los Sith.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/obi-wan.jpg" alt="Obi-Wan Kenobi">
        <h2>Obi-Wan Kenobi</h2>
        <p>Un Jedi sabio y valiente, maestro de Anakin Skywalker, que juega un papel crucial en la lucha contra el
            Imperio y la caída de los Sith.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/princesa-leia.jpg" alt="Princesa Leia">
        <h2>Princesa Leia</h2>
        <p>Una líder valiente de la Rebelión, hija de Anakin Skywalker y madre de un legado de resistencia contra el
            Imperio.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/han-solo.jpg" alt="Han Solo">
        <h2>Han Solo</h2>
        <p>Un carismático contrabandista y piloto del Halcón Milenario, que se une a la Rebelión y juega un
            papel crucial en la lucha contra el Imperio.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/chewbacca.gif" alt="Chewbacca">
        <h2>Chewbacca</h2>
        <p>Un valiente guerrero wookiee y fiel copiloto del Halcón Milenario. Es conocido por su lealtad
            inquebrantable a Han Solo y su destreza en el combate.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/yoda-sat-wars-animated-gif-3.gif" alt="Maestro Yoda">
        <h2>Maestro Yoda</h2>
        <p>Uno de los Jedi más sabios y poderosos de la historia, conocido por su enseñanza sobre la Fuerza y su
            gran conocimiento del lado luminoso.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/anakin-skywalker.jpg" alt="Anakin Skywalker">
        <h2>Anakin Skywalker</h2>
        <p>Un Jedi prodigioso que cae al lado oscuro, convirtiéndose en Darth Vader, uno de los personajes más
            complejos de la saga.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/darth_vader.jpg" alt="Darth Vader">
        <h2>Darth Vader</h2>
        <p>El temido Lord Sith, anteriormente conocido como Anakin Skywalker. Un símbolo del poder oscuro y uno de
            los villanos más emblemáticos de la saga.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/palpatine.jpg" alt="Senador Palpatine">
        <h2>Senador Palpatine</h2>
        <p>Un político astuto que manipula la República Galáctica y se convierte en el Emperador, orquestando la
            caída de los Jedi como Darth Sidious.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/dart-sidious.jpg" alt="Darth Sidious">
        <h2>Darth Sidious</h2>
        <p>También conocido como el Senador Palpatine, es un Sith oscuro que manipula la República Galáctica para
            convertirse en el Emperador del Imperio, destruyendo la Orden Jedi en el proceso.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/lando.jpg" alt="Lando Calrissian">
        <h2>Lando Calrissian</h2>
        <p>Un contrabandista y jugador carismático que se convierte en un valioso aliado de la Rebelión en la lucha
            contra el Imperio.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/mace-windu.jpg" alt="Mace Windu">
        <h2>Mace Windu</h2>
        <p>Un maestro Jedi muy poderoso, conocido por su destreza en combate y su sabiduría, y por su firmeza en las
            decisiones que toma.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/jabba.jpg" alt="Jabba El Hutt">
        <h2>Jabba El Hutt</h2>
        <p>Un señor del crimen temido en Tatooine, conocido por su crueldad y control sobre el hampa galáctico.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/qui-gon.jpg" alt="Qui- Gon Jinn">
        <h2>Qui-Gon Jinn</h2>
        <p>Un maestro Jedi que desafía las tradiciones del Consejo Jedi y es el descubridor de Anakin Skywalker.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/darth-maul.jpg" alt="Darth Maul">
        <h2>Darth Maul</h2>
        <p>Un temido Sith con una apariencia demoníaca, conocido por su destreza en combate y lealtad a Darth
            Sidious.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/dooku.jpg" alt="Conde Dooku">
        <h2>Conde Dooku</h2>
        <p>Un ex Jedi que se convierte en Sith y en líder de los Separatistas, orquestando la guerra clon.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/padme.jpg" alt="Padmé Amidala">
        <h2>Padmé Amidala</h2>
        <p>Una valiente y carismática reina de Naboo que se convierte en senadora y madre de Luke Skywalker y Leia
            Organa.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/rey.jpg" alt="Rey">
        <h2>Rey</h2>
        <p>La última esperanza de los Jedi en la lucha contra la Primera Orden, luchando por su lugar en la historia
            y el legado Jedi.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/finn.jpg" alt="Finn">
        <h2>Finn</h2>
        <p>Un ex soldado de la Primera Orden que se convierte en miembro clave de la Resistencia, luchando por la
            libertad y la justicia.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/poe-dameron.jpg" alt="Poe Dameron">
        <h2>Poe Dameron</h2>
        <p>Uno de los mejores pilotos de la Resistencia, conocido por su habilidad para volar en situaciones
            extremas y su valentía en la lucha.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/kylo-ren.jpg" alt="Kilo Ren">
        <h2>Kilo Ren</h2>
        <p>El hijo de Han Solo y Leia Organa, que lucha con su conflicto interno entre la luz y la oscuridad, siendo
            un oscuro caballero Sith.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/ben-solo.jpg" alt="Ben Solo">
        <h2>Ben Solo</h2>
        <p>Hijo de Leia Organa y Han Solo, es el líder del Orden Ren bajo el nombre de Kylo Ren, luchando entre la
            luz y la oscuridad hasta su redención.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/lider-supremo.jpg" alt="Lider Supremo Snoke">
        <h2>Lider Supremo Snoke</h2>
        <p>El misterioso líder de la Primera Orden, quien manipula a Kylo Ren en su camino hacia el lado oscuro de
            la Fuerza.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/tripio.jpg" alt="C-3PO (Tripio)">
        <h2>C-3PO (Tripio)</h2>
        <p>Un droide de protocolo especializado en traducción, que ha servido a la familia Skywalker durante toda la
            saga.</p>
    </article>
    <article class="personaje">
        <img src="../media/img/arturito.jpg" alt="R2-D2 (Arturito)">
        <h2>R2-D2 (Arturito)</h2>
        <p>Un droide astromecánico que ha estado presente en momentos clave de la historia, desde la lucha contra el
            Imperio hasta la Resistencia.</p>
    </article>
