<section class="section-spacing">

    <p>La historia de <strong>Star Wars</strong> comienza con la trilogía original (1977-1983), que sigue a <a href="../media/img/luke-skywalker.jpg" data-lightbox="Luke Skywalker Imagen">Luke Skywalker</a>, un joven granjero que descubre su destino como Jedi para luchar contra el malvado Imperio
        Galáctico. Guiado por <a href="../media/img/obi-wan.jpg" data-lightbox="Obi-Wan Kenobi Imagen">Obi-Wan Kenobi</a> y acompañado por la <a href="../media/img/princesa-leia.jpg" data-lightbox="Princesa Leia Imagen">Princesa Leia</a>, <a href="../media/img/han-solo.jpg" data-lightbox="Han Solo Imagen">Han Solo</a> y sus amigos, se une a la
        Rebelión para enfrentarse a <a href="../media/img/darth_vader.jpg" data-lightbox="Darth Vader Imagen">Darth Vader</a> y al <a href="../media/img/dart-sidious.jpg" data-lightbox="Palpatine Imagen"> Emperador Palpatine</a>. En El Retorno del Jedi, Luke redime a su
        padre, <a href="../media/img/anakin-skywalker.jpg" data-lightbox="Anakin Skywalker Imagen">Anakin Skywalker</a>, quien en un acto final destruye al Emperador, restaurando la paz en la galaxia.</p>

    <p>La trilogía de precuelas (1999-2005) explora los orígenes de <a href="../media/img/anakin-skywalker.jpg" data-lightbox="Anakin Skywalker Imagen">Anakin Skywalker</a>, un niño con un gran poder en
        la Fuerza que es entrenado como Jedi pero es seducido por el Lado Oscuro. Manipulado por el <a href="../media/img/palpatine.jpg" data-lightbox="Palpatine Imagen">Canciller Palpatine</a>, se convierte en <a href="../media/img/darth_vader.jpg" data-lightbox="Darth Vader Imagen">Darth Vader</a> tras la caída de la República y la exterminación de los Jedi en la
        Orden 66. Su transformación es completa cuando lucha contra su maestro Obi-Wan y sufre graves heridas,
        quedando atrapado en su icónica armadura negra mientras el Imperio toma el control de la galaxia.</p>

    <p>La trilogía secuela (2015-2019) presenta a una nueva generación de héroes, liderada por <a href="../media/img/rey.jpg" data-lightbox="Rey Imagen">Rey</a>, <a href="../media/img/finn.jpg" data-lightbox="Finn Imagen">Finn</a> y <a href="../media/img/poe-dameron.jpg" data-lightbox="Poe Imagen">Poe</a>,
        quienes luchan contra la Primera Orden, una organización nacida del Imperio caído. <a href="../media/img/kylo-ren.jpg" data-lightbox="Kylo Ren Imagen">Kylo Ren</a>, nieto de <a href="../media/img/darth_vader.jpg" data-lightbox="Darth Vader Imagen">Darth Vader</a>, busca completar el legado de su abuelo, pero finalmente es redimido gracias a <a href="../media/img/rey.jpg" data-lightbox="Rey Imagen">Rey</a>, quien descubre su
        conexión con el <a href="../media/img/dart-sidious.jpg" data-lightbox="Palpatine Imagen">Emperador Palpatine</a>. En El Ascenso de Skywalker, la Resistencia derrota a la Primera Orden y
        <a href="../media/img/rey.jpg" data-lightbox="Rey Imagen">Rey</a> adopta el apellido Skywalker, cerrando así la saga de la familia que marcó el destino de la galaxia.</p>

    <h4>El Origen de los Jedi</h4>
    <div>
        <a href="../media/img/Jedi.jpg" data-lightbox="Historia Star Wars" data-title="Imagen de Jedi">
            <img src="../media/img/Jedi.jpg" alt="Imagen Jedi">
        </a>
    </div>
    <p>Los Jedi surgieron hace más de 25,000 años en el planeta Tython, donde un grupo de filósofos y místicos exploraban la energía mística conocida como la Fuerza. Estos primeros usuarios de la Fuerza se llamaban Jedaii, y buscaban un equilibrio entre el Ashla (el lado luminoso) y el Bogan (el lado oscuro). Sin embargo, con el tiempo, algunos miembros comenzaron a inclinarse más hacia el Bogan, lo que generó conflictos internos. Para evitar la corrupción, los Jedaii se reorganizaron en la Orden Jedi, estableciendo un código estricto basado en la compasión, el desapego y la defensa de la paz. Se convirtieron en guardianes de la República Galáctica y usaban sables de luz como símbolo de su compromiso con la justicia.</p>

    <h4>El Nacimiento de los Sith</h4>

    <div>
        <a href="../media/img/Sith.jpg" data-lightbox="Historia Star Wars" data-title="Imagen Sith">
            <img src="../media/img/Sith.jpg" alt="Imagen Sith">
        </a>
    </div>
    <p>Hace unos 7,000 años, un Jedi llamado Ajunta Pall y otros seguidores del lado oscuro fueron exiliados
        después de la Guerra Oscura. Viajaron a un planeta lejano llamado Korriban, hogar de una especie poderosa en
        la Fuerza llamada los Sith. Los exiliados se convirtieron en sus gobernantes y mezclaron su linaje con los
        Sith nativos, formando el Imperio Sith.

        A lo largo de los siglos, los Sith desarrollaron su propio código, basado en el poder, la pasión y la
        dominación. A diferencia de los Jedi, que buscaban la paz y el equilibrio, los Sith creían que la verdadera
        fuerza venía del conflicto y la ambición.</p>
    <h4>La Caída de los Jedi y el Auge del Imperio</h4>
    <p>Miles de años después, los Sith regresaron con Darth Sidious (Palpatine), quien, en secreto, manipuló los
        eventos políticos para convertirse en Emperador y exterminar a los Jedi con la Orden 66. Esto marcó el final
        de la República y el inicio del Imperio Galáctico.</p>
    <h4>El Regreso de los Jedi</h4>
    <p>A pesar de la casi extinción de los Jedi, la esperanza resurgió con Luke Skywalker, quien, tras la caída de
        Darth Vader y el Emperador, reconstruyó la Orden Jedi, comenzando una nueva era en la galaxia.
        La lucha entre los Jedi y los Sith es un ciclo eterno de ascenso y caída, representando la batalla constante
        entre el bien y el mal, la luz y la oscuridad. Aunque los Sith han sido derrotados muchas veces, siempre
        encuentran la manera de regresar, manteniendo viva la historia de Star Wars...
    </p>
</section>

